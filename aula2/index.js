console.log(); //Função para mostrar alguma coisa na tela

/* Dentro da função console.log eu posso colocar uma variável,
    um objeto 
*/

/* Para exibir um texto dentro da função console.log,
eu preciso envolvê-lo em aspas */

console.log("Gabriel Henrique"); //Texto sempre será dentro de aspas

console.log('Gabriel Simões');

console.log(`Gabriel Nascimento`);

/* DIFERENÇA ENTRE ASPAS SIMPLES E ASPAS DUPLAS */

