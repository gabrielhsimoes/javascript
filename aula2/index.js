// console.log('Gabriel Simões')  ==> console.log("Gabriel 'Simões'") --- caso eu queira utilizar aspas dentro de aspas


/* CRASES */

// Utilizado para Template Strings

console.log(`Gabriel "Henrique" 'Simões'`) //nesse caso eu posso utlizar ambas as aspas

/*
    OBSERVAÇÃO: Todo e qualquer tipo de texto na linguagem de programação 
    é um tipo de dado String.

*/

